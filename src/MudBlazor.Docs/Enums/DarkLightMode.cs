namespace MudBlazor.Docs.Enums;

/// <summary>
/// Represents the theme preference for dark or light mode.
/// </summary>
public enum DarkLightMode
{
    /// <summary>
    /// The theme is determined by the operating system or browser.
    /// </summary>
    System = 0,

    /// <summary>
    /// Light theme is used.
    /// </summary>
    Light = 1,

    /// <summary>
    /// Dark theme is used.
    /// </summary>
    Dark = 2
}
